package week1.day1;

public class LearnOperators {
	public static void main(String[] args) {
		int a = 20;
		int b = 10;
		
		//Arithmetic Operators
		//add
		System.out.println(a+b);//20+10=30
		//sub
		System.out.println(a-b);//20-10=10
		//mul
		System.out.println(a*b);//20*10=200
		//div
		System.out.println(a/b);//20/10=2
		//mod
		System.out.println(a%b);//0
		
		//Comparison Opertors
		System.out.println(a>b); // true
		System.out.println(a<b); // false
		System.out.println(a==b);// false
		
		//Relational Operators
		System.out.println(a>b && a<b); // 20>10-> true && 20<10 -> false
		System.out.println(a>b || a<b); // t || f ->
		
	}

}
